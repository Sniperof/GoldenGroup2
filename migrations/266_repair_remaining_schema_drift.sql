CREATE TABLE IF NOT EXISTS public.device_possession_log (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  device_id integer NOT NULL,
  holder_type varchar(20) NOT NULL,
  holder_id integer,
  start_at timestamptz DEFAULT now() NOT NULL,
  end_at timestamptz,
  reason varchar(30) NOT NULL,
  notes text,
  created_by integer,
  created_at timestamptz DEFAULT now() NOT NULL,
  CONSTRAINT device_possession_holder_type_check
    CHECK (holder_type IN ('warehouse', 'technician', 'customer', 'workshop', 'supplier')),
  CONSTRAINT device_possession_period_check
    CHECK (end_at IS NULL OR end_at >= start_at),
  CONSTRAINT device_possession_reason_check
    CHECK (reason IN ('sale_delivery', 'repair_pickup', 'temporary_swap', 'retrieval', 'cancellation', 'transfer'))
);

CREATE TABLE IF NOT EXISTS public.service_agreements (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  agreement_number varchar(50),
  customer_id integer NOT NULL,
  customer_name varchar(255) NOT NULL,
  branch_id integer,
  agreement_date date NOT NULL,
  external_device_model_name varchar(255),
  external_device_serial varchar(255),
  external_device_notes text,
  maintenance_plan varchar(20),
  visits_count integer,
  fee_syp numeric DEFAULT 0 NOT NULL,
  status varchar(20) DEFAULT 'active' NOT NULL,
  start_date date,
  end_date date,
  closing_employee_id integer,
  created_by integer,
  legacy_contract_id integer,
  notes text,
  created_at timestamptz DEFAULT now() NOT NULL,
  updated_at timestamptz DEFAULT now() NOT NULL,
  CONSTRAINT service_agreements_status_check
    CHECK (status IN ('draft', 'active', 'cancelled', 'completed', 'discarded'))
);

CREATE OR REPLACE FUNCTION public.set_service_agreements_updated_at()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_service_agreements_updated_at ON public.service_agreements;
CREATE TRIGGER trg_service_agreements_updated_at
BEFORE UPDATE ON public.service_agreements
FOR EACH ROW EXECUTE FUNCTION public.set_service_agreements_updated_at();

